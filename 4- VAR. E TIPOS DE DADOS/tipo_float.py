#Tipo float: real, decimal
#Casas decimais: O separador de casas decimais é o ponto.

#É possível fazer dupla atribuição:
valor1, valor2 = 12.44, 13
print(valor1)
print(type(valor1))
print(valor2)
print(type(valor2))

#Podemos fazer cast, ou seja, transformar por exemplo um valor do tipo float para int:
print(int(valor1))
#ou também:
res = int(valor1)
print(res)
print(type(res))

#podemos trabalahar também com números complexos:
variavel = 5j
print(variavel)
print(type(variavel))  #voce vai ver que é da classe complex.



# O tipo de dado None representa o tipo sem tipo, ou poderia ser conhecido como tipo vazio, porém, falar que é um tipo sem tipo
# é mais apropriado.

numeros = None
print(numeros)
print(type(numeros))

# É utilizado o tipo None quando queremos criar uma variável e queremos inicializá-la com um tipo sem tipo
# antes de recebermos um valor final.

# O tipo None é sempre considerado False em python